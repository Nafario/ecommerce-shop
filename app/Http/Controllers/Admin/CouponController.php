use Illuminate\Http\Request;

class CouponController extends Controller
{
    /**
     * Display a listing of the resource.
     *
     * @return \Illuminate\Http\Response
     */
    public function index()
    {
        $coupons = Coupon::all('id', 'coupon_code', 'discount');
        return view('dashboard.coupon.index', compact('coupons'));
    }

    /**
     * Show the form for creating a new resource.
     *
     * @return \Illuminate\Http\Response
     */
    public function create()
    {
        return view('dashboard.coupon.create');
    }

    /**
     * Store a newly created resource in storage.
     *
     * @param  \Illuminate\Http\Request  $request
     * @return \Illuminate\Http\Response
     */
    public function store(Request $request)
    {
        $newCoupon = $request->validate([
            'coupon_code' => ['string', 'max:15', 'min:3', 'required'],
            'discount' => ['numeric', 'required', 'max:50'],
        ]);
        Coupon::create($newCoupon);
        return redirect(route('coupon.index'));
    }

